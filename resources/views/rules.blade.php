    <br> 1.2.Правила определяют порядок кинообслуживания, пропуска и нормы поведения граждан, находящихся в здании кинотеатра, которые направлены, в том числе, на предотвращение нарушений прав и свобод одних посетителей кинотеатра неправомерными действиями (бездействиями) другими посетителями кинотеатра.

