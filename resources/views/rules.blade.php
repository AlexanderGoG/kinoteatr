    <br>  7.4. ЗD очки являются собственностью кинотеатра, и посетитель несет за них полную материальную ответственность. После входа в кинозал претензии к состоянию очков не принимаются.

    После окончания сеанса, при выходе из кинозала необходимо сдать 3D очки контролеру.

    <br>  7.5.В случае невозврата или порчи очков для просмотра 3D фильмов посетителем, администрация вправе взыскать их стоимость с виновного лица.

    <br>  7.6. Кинотеатр не несет ответственности за возможный дискомфорт для глаз носителя от просмотра фильма в формате ЗD. Если у посетителей есть проблемы со зрением, перед походом в кинотеатр необходимо проконсультируйтесь с врачом. Родитель ребенка сам определяет степень возможности восприятия ЗD фильма ребенком.

    <br> 7.7. Все зрители имеют право на обращение с претензиями, по вопросам реализации услуг кинотеатром к его администрации в течение рабочего дня.

    <br>  7.8. Ответственность за нарушение авторских и смежных прав предусмотрена статьей 146 Уголовного кодекса РФ.

    <br>   7.9. В соответствии с Федеральным законом «О защите детей от информации, причиняющей вред их здоровью и развитию» и статьями 63-64 Семейного кодекса РФ, в присутствии родителей или иных законных представителей детей, достигших возраста шести лет, допускается оборот информационной продукции соответствующей классификации: информационная продукция для детей, достигших двенадцати лет. Предельным возрастом, до которого разрешено посещение киносеанса, соответствуют шестнадцатилетние дети, в присутствии родителей.

    Родители несут ответственность за воспитание и развитие своих детей. Они обязаны заботиться о здоровье, физическом, психическом, духовном и нравственном развитии своих детей. Родители являются законными представителями своих детей и выступают в защиту их прав и интересов в отношениях с любыми физическими и юридическими лицами, без специальных полномочий.

    До сведения посетителей кинотеатра доводятся соответствующие ограничения в устной, письменной форме (на информационных стендах), в виде цифр и знаков: «0+, 6+, 12+, 16+, 18+».
